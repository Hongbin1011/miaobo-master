public class MovieInfo {
    private String d_id;
    private String d_name;
    private String d_picthumb;
    private String d_remarks;
    private String d_time;

    public String getD_name() {
        return d_name;
    }

    public void setD_name(String d_name) {
        this.d_name = d_name;
    }

    public String getD_id() {
        return d_id;
    }

    public void setD_id(String d_id) {
        this.d_id = d_id;
    }

    public String getD_picthumb() {
        return d_picthumb;
    }

    public void setD_picthumb(String d_picthumb) {
        this.d_picthumb = d_picthumb;
    }

    public String getD_remarks() {
        return d_remarks;
    }

    public void setD_remarks(String d_remarks) {
        this.d_remarks = d_remarks;
    }

    public String getD_time() {
        return d_time;
    }

    public void setD_time(String d_time) {
        this.d_time = d_time;
    }
}
